    private void btnRegistroMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistroMousePressed
        // TODO add your handling code here:
        //  dispose();
        String where = "";
        if (!"".equals(txtClaveRegistroP.getText())) {
            where = "WHERE clave= '" + txtClaveRegistroP.getText() + "'";

        }
        PreparedStatement ps = null;
        ResultSet rs = null;

        String sql = "SELECT image, nombre, nivel FROM  bailarin " + where;
        System.out.println(sql);
        try {
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            rs.next();
            PagosDetalles ao = new PagosDetalles();

            Blob imageBlob = rs.getBlob(1);
            InputStream binaryStream = imageBlob.getBinaryStream(1, imageBlob.length());
            BufferedImage image = ImageIO.read(binaryStream);
            Image dimg = image.getScaledInstance(PagosDetalles.imageLabelP.getWidth(), PagosDetalles.imageLabelP.getHeight(), Image.SCALE_SMOOTH);
            //cambios
            PagosDetalles.imageLabelP.setIcon(new ImageIcon(dimg));
            PagosDetalles.txtClaveP.setText(txtClaveRegistroP.getText());
            PagosDetalles.txtNombreP.setText(rs.getString("nombre"));
            PagosDetalles.txtNivelP.setText(rs.getString("nivel"));

            ao.setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(Pagos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Pagos.class.getName()).log(Level.SEVERE, null, ex);
        }

        // new AsistenciaDetalles().setVisible(true);
        dispose();
    }//GEN-LAST:event_btnRegistroMousePressed

